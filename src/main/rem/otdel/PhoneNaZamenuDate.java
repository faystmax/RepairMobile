    public PhoneNaZamenuDate(int addOrUpdate, int PK) {
        initComponents();
        this.addOrUpdate = addOrUpdate;
        this.PK = PK;
        ResultSet resSet = null;
        //извлекаем даты телефона на замену у клиента
        try {
            resSet = RepairMobile.st.executeQuery("select "
                    + " ClientMobile.PK_ClientMobile,"
                    + " TO_CHAR(ClientMobile.DateDilivery, 'DD.MM.YYYY'),"
                    + " TO_CHAR(ClientMobile.DateReturn, 'DD.MM.YYYY')"
                    + " from ClientMobile"
                    + " where ClientMobile.PK_ClientMobile=" + PK
            );

        } catch (SQLException ex) {
            Logger.getLogger(ChooseExistClient.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {
            if (resSet.next()) {
                jDateChooserGive.setDateFormatString("dd.MM.yyyy");
                jDateChooserGet.setDateFormatString("dd.MM.yyyy");
                Date thedate = new SimpleDateFormat("dd.MM.yyyy").parse(resSet.getString(2));
                jDateChooserGive.setDate(thedate);
                JTextFieldDateEditor editor = (JTextFieldDateEditor) jDateChooserGive.getDateEditor();
                editor.setEditable(false);

                if (resSet.getString(3) != null) {
                    jDateChooserGet.setDateFormatString("dd.MM.yyyy");
                    Date thedate2 = new SimpleDateFormat("dd.MM.yyyy", Locale.ENGLISH).parse(resSet.getString(3));
                    jDateChooserGet.setDate(thedate2);
                }

                JTextFieldDateEditor editor2 = (JTextFieldDateEditor) jDateChooserGet.getDateEditor();
                editor2.setEditable(false);
            }
        } catch (SQLException | ParseException ex) {
            Logger.getLogger(PhoneNaZamenuDate.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

